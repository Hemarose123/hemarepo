##Declare two variables and use comparison operators (==, !=, ===, !==, >, <, >=, <=) to compare their values. Print the results of each comparison to the console.
x=int(input("enter the x value:"))
y=int(input("enter the y value:"))
print("x==y:",x==y)
print("x!=y:",x!=y)
print("x>y:",x>y)
print("x<y:",x<y)
print("x>=y:",x>=y)
print("x<=y:",x<=y)